public class Penugasan {
    
    public static void main(String[] args) {
        double n = 1;
        System.out.println("n1: "+n);
        n += 3;
        System.out.println("n2: "+n);
        n -= 2;
        System.out.println("n3: "+n);
        n *= 0.75;
        System.out.println("n4: "+n);
        n /= 4;
        System.out.println("n5: "+n);
        n %= 0.25;
        System.out.println("n6: "+n);
        
    }
}
